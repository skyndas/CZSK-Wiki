							<p><strong>OSCam je Open Source</strong> a tudíž je k dispozici jeho zdrojový kód.</p>
							<p>OSCam znamená <strong>O</strong>pen <strong>S</strong>ource <strong>C</strong>onditional <strong>A</strong>ccess <strong>M</strong>odul. Jeho vývoj je <strong>public</strong> (veřejný) a jedná se o jeden z největších projektů na kterém spolupracuje mnoho vývojářů z různých zemí světa už od roku cca 2002.</p>
							<p>OSCam je modulární systém, to znamená že lze při kompilaci vybrat pouze moduly, které budeme používat a tím i minimalizujeme velikost binárního souboru a zároveň pamětové požadavky na box.</p>
							<p>OSCam je napsaný v jazyce C+ a webové rozhraní (dále jen WebIf) používá html, css, javascript, jquery a AJAX.</p>
							<p>Díky tomu, že zdrojový kód OSCamu je public, vznikly z OSCamu privátní uzavřené projekty jejich zdrojové kódy nejsou známy (např. DOSCam, OSCam by blackship - česká verze pro podporu Irdeto karet, Scam ...). I přesto, že OSCam je public existují speciálně upravené verze jednotlivých programátorů, které jsou non-public (neveřejné).</p>

